 <br><br><br>
 THE APPLICATION:

Myofascial pain starts with a sudden and sustained muscular contraction in a localised area. This type of pain is focused in areas of MTPs located in one or more of the affected muscles. Active MTPs are painful, quite small (3-5mm in diameter) and highly localised hyperirritable regions with palpable taut bands of skeletal muscle.

 

Locating MTPs, even with examination by experienced health practitioners is not always reliable. An objective measure such as a skin resistance measuring device or point locator can be a useful tool to confirm manual diagnosis.

 

Skin resistance and its reciprocal, skin conductance, are terms used to identify the skin's ability to resist or transmit electric current. Pain is a primary factor influencing the skin's capacity for electricity and specifically decreasing skin resistance. The biochemical changes seen in active MTPs are a result of painful stimuli increasing the rate of blood flow and sweat secretion from sweat glands and ducts. The increased sweat content can account for variations in skin resistance. Several studies have documented the accuracy of skin conductance measurements as a method for identifying acupuncture points (REF 2).
  <br><br>
   Acupuncture points (APs) have also been associated with areas of low electrical skin resistance but are mainly located along the body's meridians. There are other points not associated with meridians but there is a strong correlation between acupuncture points and myofascial trigger points. However, acupuncture points located at trigger points "are not frequently used by acupuncturists and do not share the same clinical indications as the trigger point therapy (REF 3)"

 

There still remains plenty of debate regarding the accuracy of skin resistance readings to locate acupuncture points. One of the protocols for using Q magnet therapy is applying the devices using adhesive plasters over MTPs. From the feedback of experienced manual therapists, the use of trigger point locators has proven to be an effective tool to precisely locate hyperactive nerves where the unique field generated by Q magnets have the best physiologic effects.           

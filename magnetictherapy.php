<h3>Without getting side tracked into some of the more, shall we say unusual approaches; there are generally three theories into the application of magnets on the body.</h3>
<ol>
 <li>They are worn like jewellery to affect the energy flow in the body.</li>
 <li>You apply either the South or North pole over different parts of the body, usually the painful area or acupuncture or acupressure points or say the belly button.</li>
 <li>The magnets are placed on very specific points on the body, usually over nerves where they interrupt the pain signal. </li>
</ol>
<p>Effects purported by users and practitioners of magnetic therapy are increased circulation, reduced inflammation, correction of energy imbalances, enhanced immune function, more restful sleep, stress relief and reduced or cessation of pain.</p>
<p>While there may be some legitimacy to all three approaches, the third approach has been more thoroughly researched and has a <a href="magnetic-therapy-relevant-research.php">significant body of published data</a> to verify the therapy. The research undertaken at Vanderbilt University suggests that the common bipolar magnets that are sewn into mattresses, blankets and pillows are having negligible effect on the nerve stimulus pathways. Besides, magnets used in this way are hardly going to be placed over specific anatomical structures.</p>
<p>The image below is taken from the research paper - "Effect of steady magnetic fields on action potentials and sodium currents of sensory neurons in vitro". McLean et al, <a href="magnetic-therapy-relevant-research.php#researchpaper_em_mclean_1991">Environmental Medicine, 8: 36-45, 1991</a>. It shows the comparison of five different magnetic field arrays on nerve tissue. </p>
<p><img src="images/image011.jpg" alt="Chart" /></p>
<p>The magnetic arrays are shown to the left of the respective rows. Intensity was set to elicit an action potential (the nerve's firing) with each stimulus (PRE). After exposure to the Quadrapolar array (MAGNET), the action potential firing was blocked completely in 4 minutes 30 seconds (first row), despite increased stimulus intensity. After removal of the array (POST), action potentials reappeared and the rate increased gradually over 5 minutes 40 seconds. </p>
<p>An array of four magnets with positive poles aided limited firing completely within 4 minutes 30 seconds (second row) and an array of four negative poles blocked about 50% of action potentials in 10 minutes (third row). Recovery occurred within seconds after removal of these arrays (POST). Two magnets of alternating polarity (fourth row) and a single magnet of positive polarity (fifth row) did not block action potentials after 10 minutes.</p>

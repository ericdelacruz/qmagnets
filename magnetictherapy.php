<p>It is important to understand that a magnetic field and its depth of penetration depend on the size, shape and volume as well as the magnetic material. For instance, the earth's magnetic field is 0.5 Gauss and will affect a compass in a commercial airliner travelling at an altitude of 10,000 meters, while a small rare earth 10,000 Gauss magnet (20,000 times stronger than the earth's) will have no effect on a compass 1 meter away. Clearly there is much more to magnets than Gauss or Tesla rating.</p>
<p>All magnets have a north (positive) and south (negative) pole and hence are bipolar. In magnetic therapy the term unipolar is used to describe a bipolar magnet used in a way where only one pole is directed to the body. Multipolar is used when describing a device that has both north and south poles directed to the body which is achieved with multiple magnets. </p>
<p><img src="images/QNSNS.jpg" class="imgLeft" alt="Q Magnet" />A Q magnet is a multipolar (i.e. Quadrapolar) static magnet made of the highest quality rare earth neodymium magnetic material with a 1.35 Tesla or 13,500 Gauss rating at its strongest point and Energy Product of 45 MGOe (N45). The name Quadrapolar comes from the four alternating poles on the one face and if you turned it over all four poles would be reversed. </p>
<p>Most people think that a magnet is a magnet, but as you delve into the complex world of quantum physics, things are very different to what they appear on the surface. Magnetic fields are a vector quantity and as such have both quantity and directional values. Different magnetic materials have different properties and the size of a magnet also determines the strength and depth of penetration. Multipolar magnets are much more complex and generate magnetic field gradients which have a totally different effect on moving charged particles and as it turns out on nerve tissue than the common bipolar magnets. </p>
<p><img src="images/QF28-3H.jpg" alt="QF28-3" class="imgLeft" />From a biological and pain relieving perspective, the research on magnetic therapy conducted by neurologists at Vanderbilt Medical University is quite clear. The most important characteristic of a static magnetic field is the field gradient it generates and the steepness of those gradients. Magnetic therapy has been around for hundreds of years (see history of magnetic therapy) and the Quadrapolar or Q magnet has been the most significant advancement to date. </p>

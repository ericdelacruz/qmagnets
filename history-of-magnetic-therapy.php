<p>Long before the advancement of evidence based medicine, health practitioners used all manner of therapies and potions to treat and prevent pain. From the earliest recorded history of medicine, Greek physicians and scientists were intrigued about magnetic substances and the way in which they affected the human body. </p>
<p>During the 1980’s and 90's there was a significant body of research establishing the therapeutic properties of <a href="pemf.php">pulsed electromagnetic fields</a>. There was also credible research on flexible magnetic sheets that contained multipolar features that produced non-uniform or inhomogeneous magnetic fields. At the same time a large research project lead by neurologists Dr Robert Holcomb and Dr Michael McLean at Vanderbilt Medical University established credible pier reviewed research into the therapeutic effect of quadrapolar magnets.</p>
<p>As this research was being made public, opportunistic companies looking to exploit the market began sourcing and selling the cheapest and most common magnet available, that being the bipolar magnet. The same promising research into non-uniform or inhomogeneous magnetic fields from pulsed magnetic fields, multipolar magnets and quadrapolar magnets was also showing that the uniform or homogeneous magnetic fields produced by the common bipolar magnets did have the same effects.</p>
<p>It is unfortunate that large quantities of common bipolar magnets have been sold to an unsuspecting public piggy backing off credible research. Since bipolar magnets have only a minor therapeutic effect this has certainly tainted other credible medical devices such as Q magnets. </p>				
<p> Magnets were used and studied by some of the great doctors of yesteryear including the 16th century doctor William Gilbert who was president of the Royal College of Physicians and personal physician to Queen Elizabeth I. In fact Gilbert wrote what is widely regarded as the beginning of modern science and electricity, a book called De Magnete.</p>				
<p><img src="images/Mesmer.jpg" alt="Anton Mesmer" class="imgLeft" /><strong>Anton Mesmer</strong><br /> A pivital character in the history of magnetic therapy is the famous Austrian Physician, Franz Anton Mesmer who did extensive work on the use of magnets and developed a theory concerning the body’s own magnetic energy which he coined animal magnetism. Mesmer’s work fell into disrepute by the French medical establishment. He thought he possessed a powerful animal magnetism and that through it he could heal people by touch or even waving his hand over a crowd. These types of practices by Mesmer are the origins of the word mesmerize. Mud sticks and unfortunately Mesmer's antics placed such a stigma on magnets as a credible therapy worthy of investigation, other works in the therapeutic use of magnets were largely ignored. </p>				

<h3>Stokes and Bell</h3>
<p>For example, in 1842, Stokes and Bell (Stokes of Stokes Adam syndrome fame and Bell of Bells Palsy fame) treated a patient with shoulder pain with a very powerful 20 pound magnet and reported the patient experienced relief and restoration. They published cases such as these in a very famous two-volume treatise called Lectures on the Theory and Practice of Physics.</p>				
<h3><a href="mri.php">MRI machine</a></h3>
<p>Closer to the twentieth first century, the most significant breakthrough in modern diagnostics has come in part through the use of magnetic fields. Magnetic Resonance Imaging techniques allow doctors to virtually look inside the living body to pinpoint small structural abnormalities. Magnetic Nerve Stimulators have also contributed to the acceptance of the significant contribution magnetics offers to medicine. More recently with the research into static magnets to treat pain by neurologists at Vanderbilt Medical University throughout the 1990’s, long held paradigms are slowly shifting. As magnetic therapy gains credibility it will slowly make the transition from pseudoscience to alternative to complimentary therapy and finally to main stream medicine. </p>				
<p>Medicine has a long history of being slow to catch on, which is best illustrated by the story of Ignacio Semmelweis. Ignacio was a 19th century Hungarian obstetrician otherwise known as the “saviour of mothers” after he discovered why the mothers of baby’s delivered by doctors had 3 times the chance of dying than those delivered by midwives. Semmelweis connected the dots to the doctors who came straight from giving autopsies to delivering baby’s without washing their hands. But what became of Dr Semmelweis and his discovery? Well he was effectively driven out of the medical profession and died in an asylum and it wasn’t for another 50 years until washing hands between patients became standard clinical practice. Think of the 10,000 or 100,000’s of needless deaths that occurred due to this obstinateness. </p>				
<p>The legacy of Dr Semmelweis lives on in the Semmelweis Effect which is a metaphor for human behaviour characterized by reflex-like rejection of new knowledge because it contradicts entrenched norms, beliefs or paradigms.</p>				
<p>Just in case you thought this sort of thing couldn’t possibly happen in our day of instant communication, then spare a thought for Dr Barry Marshall from Perth, Australia who discovered that the bacterium Helicobacter pylori lived in the stomach and were the cause of stomach ulcers. This was at a time when conventional science taught that no bacterium could live in the stomach. 12 years after this discovery, exasperated and with very little recognition he resorted to infecting himself with the bacteria to bring on stomach ulcers and quickly cured himself with antibiotics. The time-lag reflects, as Professor William Doe of the Australian National University comments, <strong>"how difficult it is to change medical paradigms because everyone has a vested interest in the status quo"</strong>. The pharmaceutical industry held the biggest vested interest in ulcers. Two of its biggest all-time earners had been the acid suppressing drugs cimetidine (Tagamet) and ranitidine (Zantac). These drugs did not cure ulcers, meaning patients often needed lifelong therapy. The similarities between stomach ulcers and pain are striking if not magnified.	</p>				


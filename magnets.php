<h3>Magnet Information</h3>
<p>Provided By: <br /><a href="http://www.answers.com/topic/magnet" target="_blank"><img src="images/answersLogote.gif" alt="Answers.com" /></a><br />Free Online Dictionary, Encyclopedia, Thesaurus and much more<br />
Magnetic n.</p>
</div>

<ol>
<li>An object that is surrounded by a magnetic field and that has the property, either natural or induced, of attracting iron or steel.</li>
<li>An electromagnet.</li>
<li>A person, a place, an object, or a situation that exerts attraction.</li>
</ol>
<p>[Middle English, from Old French magnete, from Latin magnēs, magnēt-, from Greek Magnēs (lithos), Magnesian (stone), magnet, from Magnēsiā, Magnesia, an ancient city of Asia Minor.]</p>

<h3>How magnets are made:</h3>
<ol>
	<li><h3>Background</h3>
	<p>A magnet is a material that can exert a noticeable force on other materials without actually contacting them. This force is known as a magnetic force and may either attract or repel. While all known materials exert some sort of magnetic force, it is so small in most materials that it is not readily noticeable. With other materials, the magnetic force is much larger, and these are referred to as magnets. The Earth itself is a huge magnet.</p>
	<p>Some magnets, known as permanent magnets, exert a force on objects without any outside influence. The iron ore magnetite, also known as lodestone, is a natural permanent magnet. Other permanent magnets can be made by subjecting certain materials to a magnetic force. When the force is removed, these materials retain their own magnetic properties. Although the magnetic properties may change over time or at elevated temperatures, these materials are generally considered to be permanently magnetized, hence the name.</p>
	<p>Other magnets are known as electromagnets. They are made by surrounding certain materials with a coil of wire. When an electric current is passed through the coil, these materials exert a magnetic force. When the current is shut off, the magnetic force of these materials drops to nearly zero. Electromagnet materials retain little, if any, magnetic properties without a flow of electric current in the coil.</p>
	<p>All magnets have two points where the magnetic force is greatest. These two points are known as the poles. For a rectangular or cylindrical bar magnet, these poles would be at opposite ends. One pole is called the north-seeking pole, or north pole, and the other pole is called the south-seeking, or south pole. This terminology reflects one of the earliest uses of magnetic materials such as lodestone. When suspended from a string, the north pole of these first crude compasses would always "seek" or point towards the north. This aided sailors in judging the direction to steer to reach distant lands and return home.</p>
	<p>In our present technology, magnet applications include compasses, electric motors, microwave ovens, coin-operated vending machines, light meters for photography, automobile horns, televisions, loudspeakers, and tape recorders. A simple refrigerator note holder and a complex medical magnetic resonance imaging device both utilize magnets.</p>
	</li>
	
	<li><h3>History</h3>
	<p>Naturally occurring magnetic lodestone was studied and used by the Greeks as early as 500 B.C. Other civilizations may have known of it earlier than that. The word magnet is derived from the Greek name magnetis lithos, the stone of Magnesia, referring to the region on the Aegean coast in present-day Turkey where these magnetic stones were found.</p>
	<p>The first use of a lodestone as a compass is generally believed to have occurred in Europe in about A.D. 1100 to A.D. 1200. The term lodestone comes from the Anglo-Saxon meaning "leading stone," or literally, "the stone that leads." The Icelandic word is leider-stein, and was used in writings of that period in reference to the navigation of ships.</p>
	<p>In 1600, English scientist William Gilbert confirmed earlier observations regarding magnetic poles and concluded that the Earth was a magnet. In 1820, the Dutch scientist Hans Christian Oersted discovered the relationship between electricity and magnetism, and French physicist Andre Ampere further expanded upon this discovery in 1821.</p>
	<p>In the early 1900s, scientists began studying magnetic materials other than those based on iron and steel. By the 1930s, researchers had produced the first powerful Alnico alloy permanent magnets. Even more powerful ceramic magnets using rare earth elements were successfully formulated in the 1970s with further advances in this area in the 1980s.</p>
	<p>Today, magnetic materials can be made to meet many different performance requirements depending on the final application.</p>
	</li>
	
	<li><h3>Raw Materials</h3>
	<p>When making magnets, the raw materials are often more important than the manufacturing process. The materials used in permanent magnets (sometimes known as hard materials, reflecting the early use of alloy steels for these magnets) are different than the materials used in electromagnets (some-times known as soft materials, reflecting the use of soft, malleable iron in this application).</p>
	</li>
	
	<li><h3>Permanent Magnet Materials</h3>
	<p>Permanent magnet lodestones contain magnetite, a hard, crystalline iron ferrite mineral that derives its magnetism from the effect the earth's magnetic field has on it. Various steel alloys can also be magnetized. The first big step in developing more effective permanent magnet materials came in the 1930s with the development of Alnico alloy magnets. These magnets take their name from the chemical symbols for the aluminum-nickel-cobalt elements used to make the alloy. Once magnetized, Alnico magnets have between 5 and 17 times the magnetic force of magnetite.</p>
	<p>Ceramic permanent magnets are made from finely powdered barium ferrite or strontium ferrite formed under heat and pressure. Their magnetic strength is enhanced by aligning the powder particles with a strong magnetic field during forming. Ceramic magnets are comparable to Alnico magnets in terms of magnetic force and have the advantage of being able to be pressed into various shapes without significant machining.</p>
	<p>Flexible permanent magnets are made from powdered barium ferrite or strontium ferrite mixed in a binding material like rubber or a flexible plastic like polyvinyl chloride.</p>
	<p>In the 1970s, researchers developed permanent magnets made from powdered samarium cobalt fused under heat. These magnets take advantage of the fact that the arrangement of the groups of atoms, called magnetic domains, in the hexagonal crystals of this material tend to be magnetically aligned. Because of this natural alignment, samarium-cobalt magnets can be made to produce magnetic forces 50 times stronger than magnetite. Headphones for small, personal stereo systems use samarium-cobalt permanent magnets. Samarium-cobalt magnets also have the advantage of being able to operate in higher temperatures than other permanent magnets without losing their magnetic strength.</p>
	<p>Similar permanent magnets were made in the 1980s using powdered neodymium iron boron which produces magnetic forces almost 75 times stronger than magnetite. These are the most powerful permanent magnets commercially available today.</p>
	</li>
	
	<li><h3>Electromagnet Materials</h3>
	<p>Pure iron and iron alloys are most commonly used in electromagnets. Silicon iron and specially treated iron-cobalt alloys are used in low-frequency power transformers.</p>
	<p>A special iron oxide, called a gamma iron oxide, is often used in the manufacture of magnetic tapes for sound and data recording. Other materials for this application include cobalt-modified iron oxides and chromium dioxide. The material is finely ground and coated on a thin polyester plastic film.</p>
	</li>
	
	<li><h3>Other Magnetic Materials</h3>
	<p>Magnetic fluids can be made by encapsulating powdered barium ferrite particles in a single layer of molecules of a long-chain polymer plastic. The particles are then held in suspension in a liquid like water or oil. Because of the plastic encapsulation, the magnetic particles slide over each other with almost no friction. The particles are so small that normal thermal agitation in the liquid keeps the particles from settling. Magnetic fluids are used in several applications as sealants, lubricants, or vibration damping materials.</p>
	</li>
	
	<li><h3>The Manufacturing Process</h3>
	<p>Just as the materials are different for different kinds of magnets, the manufacturing processes are also different. Many electromagnets are cast using standard metal casting techniques. Flexible permanent magnets are formed in a plastic extrusion process in which the materials are mixed, heated, and forced through a shaped opening under pressure.</p>
	<p>Some magnets are formed using a modified powdered metallurgy process in which finely powdered metal is subjected to pressure, heat, and magnetic forces to form the final magnet. Here is a typical powdered metallurgy process used to produce powerful neodymium-iron-boron permanent magnets with cross-sectional areas of about 3-10 square inches (20-65 sq cm):</p>
	</li>
	<li><h3>Preparing the powdered metal</h3>
		<ul>
			<li>The appropriate amounts of neodymium, iron, and boron are heated to melting in a vacuum. The vacuum prevents any chemical reaction between air and the melting materials that might contaminate the final metal alloy.</li>
			<li>Once the metal has cooled and solidified, it is broken up and crushed into small pieces. The small pieces are then ground into a fine powder in a ball mill.</li>
		</ul>
	</li>
	<li><h3>Pressing</h3>
		<p>The powdered metal is placed in a mold, called a die, that is the same length and width (or diameter, for round magnets) as the finished magnet. A magnetic force is applied to the powdered material to line up the powder particles. While the magnetic force is being applied, the powder is pressed from the top and bottom with hydraulic or mechanical rams to compress it to within about 0.125 inches (0.32 cm) of its final intended thickness. Typical pressures are about 10,000 psi to 15,000 psi (70 MPa to 100 MPa). Some shapes are made by placing the powdered material in a flexible, air-tight, evacuated container and pressing it into shape with liquid or gas pressure. This is known as isostatic compaction.</p>
	</li>
	<li><h3>Heating</h3>
		<p>The compressed "slug" of powdered metal is removed from the die and placed in an oven. The process of heating compressed powdered metals to transform them into fused, solid metal pieces is called sintering. The process usually consists of three stages. In the first stage, the compressed material is heated at a low temperature to slowly drive off any moisture or other contaminants that may have become entrapped during the pressing process. In the second stage, the temperature is raised to about 70-90% of the melting point of the metal alloy and held there for a period of several hours or several days to allow the small particles to fuse together. Finally, the material is cooled down slowly in controlled, step-by-step temperature increments.</p>
	</li>
	<li><h3>Annealing</h3>
		<p>The sintered material then undergoes a second controlled heating and cooling process known as annealing. This process removes any residual stresses within the material and strengthens it.</p>
	</li>
	<li><h3>Finishing</h3>
		<p>The annealed material is very close to the finished shape and dimensions desired. This condition is known as "nearnet" shape. A final machining process removes any excess material and produces a smooth surface where needed. The material is then given a protective coating to seal the surfaces.</p>
	</li>
	
	<li><h3>Magnetizing</h3>
		<p>Up to this point, the material is just a piece of compressed and fused metal. Even though it was subjected to a magnetic force during pressing, that force didn't magnetize the material, it simply lined up the loose powder particles. To turn it into a magnet, the piece is placed between the poles of a very powerful electromagnet and oriented in the desired direction of magnetization. The electromagnet is then energized for a period of time. The magnetic force aligns the groups of atoms, or magnetic domains, within the material to make the piece into a strong permanent magnet.</p>
	</li>
	
	<li><h3>Quality Control</h3>
		<p>Each step of the manufacturing process is monitored and controlled. The sintering and annealing processes are especially critical to the final mechanical and magnetic properties of the magnet, and the variables of time and temperature must be closely controlled.</p>
	</li>
	
	<li><h3>Hazardous Materials, Byproducts, and Recycling</h3>
		<p>Barium and the barium compounds used to make barium ferrite permanent magnets are poisonous and are considered toxic materials. Companies making barium ferrite magnets must take special precautions in the storage, handling, and waste disposal of the barium products.</p>
		<p>Electromagnets can usually be recycled by salvaging the component iron cores and copper wiring in the coil. Partial recycling of permanent magnets may be achieved by removing them from obsolete equipment and using them again in similar new equipment. This is not always possible, however, and a more comprehensive approach to recycling permanent magnets needs to be developed.</p>
	</li>
	
	<li><h3>The Future</h3>
		<p>Researchers continue to search for even more powerful magnets than those available today. One of the applications of more powerful permanent magnets would be the development of small, high-torque electric motors for battery-powered industrial robots and laptop computer disk drives. More powerful electromagnets could be used for the levitation and propulsion of high-speed trains using pulsed magnetic fields. Such trains, sometimes called maglev trains, would be supported and guided by a central, magnetic "rail." They would move without ever contacting the rail, thus eliminating mechanical friction and noise. Pulsed magnetic fields could also be used to launch satellites into space without relying on expensive and heavy booster rockets.</p>
		<p>More powerful magnets could also be used as research tools to develop other new materials and processes. Intense, pulsed magnet fields are currently being used in nuclear fusion research to contain the hot, reacting nuclear plasma that would otherwise melt any solid material vessel. Magnetic fields can also be used in materials research to study the behavior of semiconductors used in electronics to determine the effects of making micro-sized integrated circuits.</p>
	</li>


    <h3>How do Q magnets work?</h3>
<p>Precise details of the molecular mechanism of biological effects of Qmagnets
      are still under investigation. Extensive investigation since 1986 suggests
      that steep variation of the field gradient in the area encompassed by the four
      magnetic poles of the Q magnet may alter the configuration of ion channels
      and change the permeability or neuronal cell membranes to sodium and calcium
      ions. This results in suppressing transmission of painful impulses to the brain.
      Other actions are under investigation, including effects on enzyme kinetics
      and membrane lipids.</p>

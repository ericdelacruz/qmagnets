<h3>What is an Action Potential?</h3>
    <p>An Action Potential is transmitted along nerve fibres and is the basic unit
      of pain signal that transmits to the brain. The Resting Membrane Potential
      (measured in mV) is the measured potential difference across a cell membrane
      of a sensory neuron or nerve cell. A variety of cellular permeable ions such
      as Na+, Ca++, K+ and Cl- will attempt to drive the membrane potential towards
      its electrochemical equilibrium. Once the membrane potential reaches the Threshold
      Potential, an Action Potential is inevitable, unless the nerve cell is being
      exposed to a steep magnetic field such as that produced by the Q magnet. An
      Action Potential consists of a rapid Depolarisation (membrane potential becomes
      less –ve) followed by a Repolarisation (membrane potential becomes less +ve).</p>

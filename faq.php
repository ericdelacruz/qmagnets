<h3>What are Q magnets?</h3>
<h3 class="noticemegray">What is a Q magnet?</h3>
<a href="products.php#QF28-6"><img src="images/QF28-6.jpg" alt="QF28-6 Q magnet" width="200" height="200" border="2" align="left" class="left-img" /></a></h3>
<p>The
      Quadrapolar or Q magnet is a static multipolar magnetic flux generator Class I Medical Device. It
      consists of four alternating magnetic quadrants or poles within one solid magnetic
      body. The Q magnet  produces  <a href="magnetic-field-gradients.php">steep magnetic field gradients</a> along the boundaries
      of the four alternating pole quadrants.  The Q magnet has a <a href="flux-plate.php">flux
      plate</a> attached to the top of the device to further increase the field gradient
      and drive the field deeper into the body to enhance the therapeutic effect
      and target the deeper nerve structures. </p>
<p>On top of the plate is a durable sticker that displays the orientation arrow
    and a unique serial number for the medical device. The device is then encased in a hypoallergenic tough acrylic casing. </p>
<p><img src="images/qmagcomp.jpg" alt="Q magnet comparisons" width="211" height="675" border="2" align="left" class="left-img" /></p>
<p>Seen
  through a magnet viewer to the left, the boundaries between the four alternating
  pole quadrants can be observed. The boundaries, seen as lines, represent the
   dividing point where the field as measured on the surface of the device transitions
  from one pole to the next. The field gradient makes this steep transition along
  the entire length of the boundary. </p>
<p>The first Quadrapolar magnets used therapeutically were comprised
  of four separate bipolar magnets which had to be assembled into one device. The
  maximally effective region of these devices were at the point of contact of each
  bipolar magnet. The bottom image of the three to the left shows the four alternating
  bipolar magnets through a magnet viewer. Although the magnets are in fact touching,
  looking at the magnetic field it appears they are slightly separated. This is also
  the dividing  point where the magnetic field transitions from one pole to the next. </p>
<p>Q magnets provide a far superior effect as they produce a steeper field gradient
  along the entire length of inter-pole boundary. </p>
<p>Q magnets were designed
  to treat acute and chronic and postoperative pain and swelling. Work on the
  first  Quadrapolar magnets was pioneered  by Dr Robert Holcomb in 1986. Q magnets
  are  manufactured in Australia and is listed with the Therapeutic Goods
Administration as a Class I Medical Device AUSTR # 132324.</p>

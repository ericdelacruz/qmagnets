<h3>What is a placebo effect?</h3>
    <p>Many effects of a device or medication may only be perceived, and if the relief
      is short lived or can not be sustained, then the effect is placebo. A placebo
      effect may occur with any treatment. Clinical trials are usually double blinded
      placebo controlled studies to see the effectiveness of a new drug or device.
      When a device or medication is tested against a placebo, the real effect of
      the device or medication is measured. With any treatment, a person may experience
      relief that is very short in duration. If a positive effect of a treatment
      can not be duplicated or sustained, it may be a placebo effect. </p>
<h3>Most treatments for pain have a very high placebo effect. Could the effect
      of the Q magnet be a placebo?</h3>
    <p>The treatment of pain with magnetic treatment devices and with pharmaceuticals
      involve placebo effects. Placebo effects have been a great confounder and a
      substantial component of pain relief in placebo controlled studies of pharmaceuticals.
      Control studies have shown that the efficacy of Quadrapolar magnetic devices
      is greater than that of placebo devices in those studies. Thus, the pain relief

<h3>What is the mechanism of action?</h3>
    <p>The precise molecular mechanism of action has not been completely established.
      Simultaneous blockade of voltage-sensitive sodium dependent action potentials
      and ligand-activated calcium responses of cultured neurons are consistent with
      an effect of the Quadrapolar generated magnetic field on gating dipoles within
      the transmembrane portions of the ion channel sequences. The effects in a number
      of biological systems, including cultured cells and animal models, suggest
      that there may be multiple simultaneous mechanisms of action.</p>
<h3>What is the “Sweet Spot” of the Q magnet?</h3>
